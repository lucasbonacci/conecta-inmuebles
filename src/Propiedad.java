public class Propiedad {

    private int id;
    private String ubicacion;
    private float precio;
    private float tamano;
    private String fotos;
    private String estado;

    public Propiedad(int id, String ubicacion, float precio, float tamano, String fotos, String estado) {
        this.id = id;
        this.ubicacion = ubicacion;
        this.precio = precio;
        this.tamano = tamano;
        this.fotos = fotos;
        this.estado = estado;
    }


}

